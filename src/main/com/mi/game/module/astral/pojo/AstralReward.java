package com.mi.game.module.astral.pojo;

public class AstralReward {
	private int pid;
	private int num;
	private int state; //是否领取 0未领 1已领
	
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}

	
	
}
